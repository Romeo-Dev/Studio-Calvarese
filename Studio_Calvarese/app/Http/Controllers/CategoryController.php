<?php

namespace App\Http\Controllers;

use App\Category;
use Illuminate\Http\File;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;

class CategoryController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data['categories']=Category::all();
        return view('dashboard.categorie',$data);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {

    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */

    public function store(Request $request)
    {
        $Category= new Category();
        $Category->categoria = $request->categoria;
        $Category->save();

       return redirect()->back()->with('alert','Categoria aggiunta con successo');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $data['category'] = Category::all()->find($id);

        return view('dashboard.editCategory',$data);

    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id = 0)
    {
        $id = $request->id;
        if ($request->categoria != null){
            DB::table('categories')
                ->where('id','=',$id)
                ->update(['categoria' => $request->categoria]);
            return redirect('/dash/categories')->with('alert','Categoria aggiornata con successo');
        }else
            return redirect()->back()->with('alert','Inserire il nuovo nome della categoria');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Category::destroy($id);

        return redirect()->back()->with('alert','Categoria eliminata con successo');

    }
}
